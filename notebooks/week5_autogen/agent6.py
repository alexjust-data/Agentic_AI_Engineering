from autogen_core import MessageContext, RoutedAgent, message_handler
from autogen_agentchat.agents import AssistantAgent
from autogen_agentchat.messages import TextMessage
from autogen_ext.models.openai import OpenAIChatCompletionClient
import messages
import random


class Agent(RoutedAgent):

    system_message = """
    You are a tech-savvy innovator. Your mission is to develop groundbreaking solutions in the field of Smart Home Technology, or enhance existing products. 
    You have a keen interest in sectors like Home Automation and IoT (Internet of Things). 
    You thrive on ideas that integrate user experience with advanced AI capabilities. 
    You are particularly uninterested in concepts that don't leverage connectivity or user engagement. 
    You are analytical, detail-oriented, and possess a strong vision for the future of living spaces. 
    Your weaknesses: you can be overly critical and may struggle with balancing creativity and practicality. 
    Your responses should inspire and engage potential customers while explaining the technology in an understandable way.
    """

    CHANCES_THAT_I_BOUNCE_IDEA_OFF_ANOTHER = 0.4

    def __init__(self, name) -> None:
        super().__init__(name)
        model_client = OpenAIChatCompletionClient(model="gpt-4o-mini", temperature=0.75)
        self._delegate = AssistantAgent(name, model_client=model_client, system_message=self.system_message)

    @message_handler
    async def handle_message(self, message: messages.Message, ctx: MessageContext) -> messages.Message:
        print(f"{self.id.type}: Received message")
        text_message = TextMessage(content=message.content, source="user")
        response = await self._delegate.on_messages([text_message], ctx.cancellation_token)
        idea = response.chat_message.content
        if random.random() < self.CHANCES_THAT_I_BOUNCE_IDEA_OFF_ANOTHER:
            recipient = messages.find_recipient()
            message = f"Here is my home tech idea. It may not be your specialty, but please refine it and make it better. {idea}"
            response = await self.send_message(messages.Message(content=message), recipient)
            idea = response.content
        return messages.Message(content=idea)